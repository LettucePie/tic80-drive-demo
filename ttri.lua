usize = 32
vsize = 32
function TIC()
  cls(1)
  if btn(0) then usize=usize-1 end
  if btn(1) then usize=usize+1 end
  if btn(2) then vsize=vsize-1 end
  if btn(3) then vsize=vsize+1 end

-- draw a scaling view into the map ram

  ttri(30,0,
         90,0,
         0,64,
         0,0,
         usize,0,
         0,vsize,
         0, 14)
  ttri(90,0,
         0,64,
         120,64,
         usize,0,
         0,vsize,
         usize,vsize,
         0, 14)
end
